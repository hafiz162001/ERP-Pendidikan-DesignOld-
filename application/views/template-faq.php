- Share room ke sosial media untuk mengundang peserta meeting,<br>
- Jika sudah share room, masuk ke menu "Lihat Room" untuk memulai meeting,<br>
- Isi Nama Display dan Room Password (Moderator), lalu klik Join Room,<br>
- Selanjutnya pilih koneksi Audio, apakah Anda ingin menggunakan Mikropon atau hanya mendengarkan,<br>
