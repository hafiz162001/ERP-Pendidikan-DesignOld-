  Anda dapat mengakses fitur share screen, share webcam, chatting, upload presentation, share external video, dan polling.<br>
Untuk cara penggunaannya sebagai berikut :<br>
- <b>Share Screen</b> : Anda dapat menggunakan fitur share screen pada button "Share Your Screen" yang terdapat di bawah tampilan meeting BISA Tampil.<br><br>

- <b>Share Webcam</b> : Anda dapat menggunakan fitur share webcam pada button "Share Webcam" yang terdapat di bawah tampilan meeting BISA Tampil, saat muncul tampilan Webcam Setting, pilih Camera yang akan digunakan beserta Quality nya.<br><br>

- <b>Chatting</b> : Anda dapat menggunakan fitur Public Chat yang terdapat di pojok kiri atas dan Private Chat dengan cara klik user yang akan Anda chat secara pribadi, lalu klik "start private chat"<br><br>

- <b>Upload Presentation</b> : Klik icon plus (+) yang ada dipojok bawah, lalu klik "Upload a Presentation", lalu drag/browse file presentasi yang akan di upload, lalu klik Upload.<br><br>

- <b>Share External Video</b> : Klik icon plus (+) yang ada dipojok bawah, lalu klik "Share an External Video", Isi link video yang akan di-share, kemudian klik "Share a new video".<br><br>

- <b>Polling</b> : Klik icon plus (+) yang ada dipojok bawah, lalu klik "Start a poll", Lalu pilih polling seperti apa yang akan digunakan, lalu tunggu hingga peserta mengisi polling tersebut. Untuk menampilkan polling, klik "Publish Polling Results".<br><br>
